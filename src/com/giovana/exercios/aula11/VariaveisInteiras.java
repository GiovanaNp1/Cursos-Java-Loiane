package com.giovana.exercios.aula11;

public class VariaveisInteiras {

	public static void main(String[] args) {
		byte idade1  = 20;
		
		short idade2  =  21;
		
		int idade3 = 22;
		
		long idade4 = 23;
		
		System.out.println("Valor da variavel idade1: " +  idade1);
		System.out.println("Valor da variavel idade2: " +  idade2);
		System.out.println("Valor da variavel idade3: " +  idade3);
		System.out.println("Valor da variavel idade4: " +  idade4);
	}

}
